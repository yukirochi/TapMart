const mongoose = require("mongoose");

const Schema = mongoose.Schema;

let Userschema = new Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model("User", Userschema, "user")
